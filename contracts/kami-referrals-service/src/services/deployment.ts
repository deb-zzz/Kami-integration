// Contract bytecode - this will be generated by Hardhat compilation
// For now, we'll read it from artifacts after compilation
export async function deployContract(initialUri: string): Promise<Address> {
  try {
    // Read the compiled contract bytecode from Hardhat artifacts
    const artifactPath = path.join(
      __dirname,
      "../../artifacts/contracts/KAMI-Sigil.sol/KamiSigils.json"
    );

    let bytecode: string;
    try {
      const artifact = JSON.parse(fs.readFileSync(artifactPath, "utf8"));
      bytecode = artifact.bytecode;
    } catch (error) {
      throw new Error(
        "Contract not compiled. Please run 'npm run compile' first."
      );
    }

    const client = await walletClient;
    const account = await getAccount();
    const hash = await client.deployContract({
      account,
      chain,
      abi: contractABI,
      bytecode: bytecode as `0x${string}`,
      args: [initialUri],
    });

    // Wait for transaction receipt
    const receipt = await publicClient.waitForTransactionReceipt({ hash });

    if (!receipt.contractAddress) {
      throw new Error("Contract deployment failed: no contract address");
    }

    return receipt.contractAddress;
  } catch (error: any) {
    throw new Error(`Deployment failed: ${error.message}`);
  }
}

